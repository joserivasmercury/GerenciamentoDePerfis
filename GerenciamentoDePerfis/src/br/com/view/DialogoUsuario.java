    private void preparar(Usuario usuario) {
        cargoControl = CargoControl.getInstancia();
        perfilControl = PerfilControl.getInstancia();
        usuarioControl = UsuarioControl.getInstancia();
        comboCargo.setModel(new javax.swing.DefaultComboBoxModel(cargoControl.getCargoNames()));
        Integer[] dias = new Integer[111];
        for (int i = 0; i < 111; i++) {
            dias[i] = i + 1900;
        }
        comboAno.setModel(new DefaultComboBoxModel(dias));
        comboMes.addItemListener(this::comboDataChanged);
        comboAno.addItemListener(this::comboDataChanged);
        updateLista(listPerfil, perfilControl.getPerfilNames());
        if (usuario == null) {
            this.setTitle("Novo usuário");
            botaoProsseguir.setText("Adicionar Usuário");
        } else {
            this.setTitle(usuario.getNome() + " - Registrado em " + usuario.getDataCriado());
            botaoProsseguir.setText("Alterar Usuário");
            usuarioOriginal = usuario;
            carregarUsuario(usuario);
        }
    }
    
    private void updateLista(JList lista, String[] valores) {
        lista.removeAll();
        lista.setModel(new javax.swing.AbstractListModel() {
            String[] strings = valores;

            public int getSize() {
                return strings.length;
            }

            public Object getElementAt(int i) {
                return strings[i];
            }
        });
    }
    
    private void comboDataChanged(ItemEvent e) {
        if (e == null || e.getStateChange() == ItemEvent.SELECTED) {
            int dia = comboDia.getSelectedIndex() + 1;
            int mes = comboMes.getSelectedIndex() + 1;
            int ano = comboAno.getSelectedIndex() + 1900;
            int maxDias = 30;
            if (mes == 2) {
                if (ano % 4 == 0) {
                    maxDias = 29;
                } else {
                    maxDias = 28;
                }
            } else if ((mes < 6 && mes % 2 == 1)
                     || mes > 6 && mes % 2 == 0) {
                maxDias = 31;
            }
            Integer[] dias = new Integer[maxDias];
            for (int i = 0; i < maxDias; i++) {
                dias[i] = i + 1;
            }
            comboDia.setModel(new DefaultComboBoxModel(dias));
            if (dia > maxDias) {
                comboDia.setSelectedIndex(maxDias - 1);
            } else {
                comboDia.setSelectedIndex(dia - 1);
            }
        }
    }

