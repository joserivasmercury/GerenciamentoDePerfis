    private void botaoProsseguirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoProsseguirActionPerformed
        String nome = campoNome.getText();
        String cpf = campoCpf.getText().trim();
        int dia = comboDia.getSelectedIndex() + 1;
        int mes = comboMes.getSelectedIndex() + 1;
        int ano = comboAno.getSelectedIndex() + 1;
        char sexo = (char) comboSexo.getSelectedIndex();
        Date data = new Date(ano, mes, dia);
        Cargo cargo = cargoControl.getByNome((String) comboCargo.getSelectedItem());
        List<Perfil> perfis = getPerfis();
        boolean ok = validar(nome, cpf);
        if (ok) {
            labelWarnCpf.setText("");
            labelWarnNome.setText("");
            labelWarnPerfil.setText("");
            if (usuarioOriginal == null) {
                usuarioControl.addUsuario(nome, cpf, data, sexo, cargo, perfis);
                JOptionPane.showMessageDialog(null, "Usuário adicionado com sucesso!", "Adicionar usuário", JOptionPane.INFORMATION_MESSAGE);
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(null, "Usuário alterado!", "Alterar usuário", JOptionPane.INFORMATION_MESSAGE);
                usuarioControl.updateUsuario(usuarioOriginal.getCpf(), nome, cpf, data, sexo, cargo, perfis);
                this.dispose();
            }
        }
    }//GEN-LAST:event_botaoProsseguirActionPerformed

    private boolean validar(String nome, String cpf) {
        boolean ok = true;
        if (nome.trim().equals("")) {
            ok = false;
            labelWarnNome.setText("Insira o nome");
        } else {
            labelWarnNome.setText("");
        }
        if (cpf.trim().equals("")) {
            ok = false;
            labelWarnCpf.setText("Insira o CPF");
        } else if (usuarioControl.getByCPF(cpf) != null) {
            if (usuarioOriginal != null && !cpf.equalsIgnoreCase(usuarioOriginal.getCpf())) {
                ok = false;
                labelWarnCpf.setText("Já existe");
            }
        } else {
            labelWarnCpf.setText("");
        }
        if (listPerfil.getSelectedValuesList().isEmpty()) {
            ok = false;
            labelWarnPerfil.setText("Escolha pelo menos 1 item abaixo");
        } else {
            labelWarnPerfil.setText("");
        }
        return ok;
    }
    
    private List<Perfil> getPerfis() {
        List<String> perfilNomes = listPerfil.getSelectedValuesList();
        List<Perfil> perfis = new ArrayList<>();
        for (String perfilNome : perfilNomes) {
            perfis.add(perfilControl.getByNome(perfilNome));
        }
        return perfis;
    }
    
    private void carregarUsuario(Usuario usuario) {
        campoNome.setText(usuario.getNome());
        campoCpf.setText(usuario.getCpf());
        comboAno.setSelectedIndex(usuario.getDatanasc().getYear() - 1);
        comboMes.setSelectedIndex(usuario.getDatanasc().getMonth() - 1);
        comboDataChanged(null);
        comboDia.setSelectedIndex(usuario.getDatanasc().getDate() - 1);
        comboSexo.setSelectedIndex(usuario.getSexo());
        
        String cargo = usuario.getCargo().getNome();
        for (int i = 0; i < comboCargo.getModel().getSize(); i++) {
            if (cargo.equals((String) comboCargo.getModel().getElementAt(i))) {
                comboCargo.setSelectedIndex(i);
            }
        }
        
        String[] perfis = usuario.getListaPerfis();
        int[] indicesPerfil = new int[perfis.length];
        for (int i = 0; i < perfis.length; i++) {
            for (int j = 0; j < listPerfil.getModel().getSize(); j++) {
                if (perfis[i].equals((String) listPerfil.getModel().getElementAt(j))) {
                    indicesPerfil[i] = j;
                    break;
                }
            }
        }
        listPerfil.setSelectedIndices(indicesPerfil);
    }
    
