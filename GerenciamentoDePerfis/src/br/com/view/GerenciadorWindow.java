    private void refresh() {
        updateLista(listCargos, cargoControl.getCargoNames());
        updateLista(listPerfis, perfilControl.getPerfilNames());
        updateLista(listUsuarios, usuarioControl.getUsuarioNames());
        updateContadores();
    }
    
    private void updateContadores() {
        labelContCargo.setText(cargoControl.countCargos() + "");
        labelContPerfil.setText(perfilControl.countPerfis() + "");
        labelContUsuario.setText(usuarioControl.countUsuarios() + "");
    }
    
    private void updateLista(JList lista, String[] valores) {
        lista.removeAll();
        lista.setModel(new javax.swing.AbstractListModel() {
            String[] strings = valores;

            public int getSize() {
                return strings.length;
            }

            public Object getElementAt(int i) {
                return strings[i];
            }
        });
    }
    
    private int selecionarOpcao() {
        String[] opcoes = {"Editar", "Excluir"};
        int opcao = -1;
        try {
            opcao = JOptionPane.showOptionDialog(null, "Deseja editar ou excluir este item?", 
                    "Opções", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE, null, opcoes, 0);
        } catch (Exception e) {
            
        }
        return opcao;
    }
    
