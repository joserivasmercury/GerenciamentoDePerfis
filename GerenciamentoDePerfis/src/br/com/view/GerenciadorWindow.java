    private void listUsuariosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_listUsuariosMouseClicked
        if (evt.getClickCount() == 2 && listUsuarios.getSelectedIndex() >= 0) {
            System.out.println(listUsuarios.getSelectedValue());
            String[] exploded = listUsuarios.getSelectedValue().toString().split(" ");
            int cpfPos = exploded.length - 1;
            String cpf = exploded[cpfPos];
            int opcao = selecionarOpcao();
            if (opcao == 0) {
                Usuario usuario = usuarioControl.getByCPF(cpf);
                DialogoUsuario.alterarUsuario(this, usuario);
                refresh();
            } else if (opcao == 1) {
                opcao = -1;
                try {
                    opcao = JOptionPane.showConfirmDialog(null, "Tem certeza que deseja excluir esse usuário?",
                            "Excluir usuário", JOptionPane.YES_NO_OPTION, JOptionPane.WARNING_MESSAGE);
                } catch (Exception e) {
                }
                if (opcao == JOptionPane.YES_OPTION && usuarioControl.deleteUsuario(cpf)) {
                    refresh();
                    JOptionPane.showMessageDialog(null, "Usuário excluído!", "Excluir usuário", JOptionPane.INFORMATION_MESSAGE);
                }
            }
        }
    }//GEN-LAST:event_listUsuariosMouseClicked

